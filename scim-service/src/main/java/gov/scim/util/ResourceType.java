package gov.scim.util;

public class ResourceType {
	

	 /**
	   * A SCIM resource type will return a static value that
	   * describes the User SCIM resource type specification.
	   */
	  public static final String USER = "User";
	  
	  /**
	   * A SCIM resource type will return a static value that
	   * describes the Entitlement SCIM resource type specification.
	   */
	  public static final String ENTITLEMENT = "Entitlement";
	  
	  /**
	   * An extended SCIM resource type will return a static value that
	   * describes the Security SCIM resource type specification.
	   */
	  public static final String SECURITY = "Security";
	  
	  

}
